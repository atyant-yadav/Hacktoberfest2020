public class OP
{
  public static void main(String[] args)
  
   {
     int a = 5;
     int b = 7;

     System.out.println("a AND b = " + (a&b));
     System.out.println("a OR b =" + (a|b));
     System.out.println("a XOR b = " + (a^b));
     System.out.println("a NOT = " + ~a);
 }

}